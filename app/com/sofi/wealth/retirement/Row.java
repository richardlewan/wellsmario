package com.sofi.wealth.retirement;

public class Row {
    private int year;
    private double wellsBalance;
    private double sofiBalance;

    public int getYear() {
        return year;
    }
    public void setYear(int year) {
        this.year = year;
    }
    public double getWellsBalance() {
        return wellsBalance;
    }
    public void setWellsBalance(double wellsBalance) {
        this.wellsBalance = wellsBalance;
    }
    public double getSofiBalance() {
        return sofiBalance;
    }
    public void setSofiBalance(double sofiBalance) {
        this.sofiBalance = sofiBalance;
    }

}
